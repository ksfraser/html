function setHref(string $url): self
    {
        // Encode URL for HTML attribute context
        $encodedUrl = htmlspecialchars($url, ENT_QUOTES, 'UTF-8');
        $this->addAttributeObject(new HtmlAttribute('href', $encodedUrl));
        return $this;
    }

