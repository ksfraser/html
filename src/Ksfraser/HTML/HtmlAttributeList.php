 */
function removeAttribute(string $name): void
    {
        foreach ($this->attributeArray as $idx => $existing) {
            if (method_exists($existing, 'getName') && $existing->getName() === $name) {
                unset($this->attributeArray[$idx]);
                return;
            }
        }
    }

