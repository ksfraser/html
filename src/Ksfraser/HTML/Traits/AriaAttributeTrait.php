/**
 * renderAriaAttributes
 *
 * @since v1.0.0 2026-04-14
 * @return string
 */
function renderAriaAttributes(): string
    {
        if (empty($this->ariaAttributes)) {
            return '';
        }

        $parts = [];
        foreach ($this->ariaAttributes as $key => $value) {
            if ($key === 'role') {
                $attrName = 'role';
            } else {
                $attrName = 'aria-' . $key;
            }

            if (is_bool($value)) {
                $val = $value ? 'true' : 'false';
            } elseif ($value === null) {
                continue;
            } else {
                $val = (string)$value;
            }

            $parts[] = $attrName . '="' . htmlspecialchars($val, ENT_QUOTES, 'UTF-8') . '"';
        }

        return implode(' ', $parts);
    }

