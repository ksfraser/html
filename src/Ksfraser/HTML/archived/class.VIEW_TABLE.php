 */
function toHtml()
    {
        $html = '';
        $html .= $this->start_table();
        foreach ($this->table_headers as $header) {
            $html .= $header->toHtml();
        }
        foreach ($this->table_rows as $row) {
            $html .= $row->toHtml();
        }
        $html .= $this->end_table();

        // Check for empty content and return self-closing tag if applicable
        return trim($html) === '' ? '<table />' : $html;
    }
}
