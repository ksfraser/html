	{
		switch( $var )
		{
			case "style_width":
				if( is_integer( $value ) )
				{
					//good start
				}
				else
				{
					//try to convert data type
					$tmp = (int)$value;
					if( is_integer( $tmp ) )
					{
						if( $tmp <> $value )
						{
							return FALSE;
						}
						$value = $tmp;
					}
				}
			default:
				parent::set( $var, $value, $enforce);
				break;
		}
	}
