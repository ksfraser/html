 *
 * @since v1.0.0 2026-04-14
 * @param mixed $name
 * @return void
 */
function removeAttributeByName( string $name )
	{
		if( isset( $this->attributes ) && is_array( $this->attributes ) )
		{
			foreach( $this->attributes as $key => $attr )
			{
				if( $attr->getName() === $name )
				{
					unset( $this->attributes[$key] );
					return;
				}
			}
		}
	}
