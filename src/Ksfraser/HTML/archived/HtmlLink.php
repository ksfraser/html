function setTarget( $target )
/**
 * setTarget()
 *
 * @since v1.0.0 2026-04-14
 * @param mixed $target
 * @return void
 */
	{
		//Target can be _self, _blank, _parent, _top
		switch( $target )
		{
			case '_self':
			case '_blank':
			case '_parent':
			case '_top':
				$this->addAttribute( new HtmlAttribute( "target", $target ) );
				break;
			default:
				throw new Exception( "Target type not recognized: $target" );
		}
		return;
	}

